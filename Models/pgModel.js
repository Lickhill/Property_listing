const mongoose = require('mongoose');


const pgSchema = new mongoose.Schema({
  name:{
    type:String,
    trim:true,
    required:true
  },
  address:{
    type:String,
    trim:true,
    required:true
  },
  city:{
    type:String,
    trim:true,
    required:true
  },
  state:{
    type:String,
    trim:true,
    required:true
  },
  pincode:{
    type:Number,
    trim:true,
    required:true
  },
  discription:{
    type:String,
    required:true
  },
  type:{
    type:String,
    trim:true,
    required:true,
    enum:["Boys","Girls","Co-ed"]
  },
  rent:{
    type:Number,
    trim:true,
    required:true
  },
  security_deposit:{
    type:Number,
    trim:true,
  },
  contact:{
    type:String,
    trim:true,
    required:true
  },
  email:{
    type:String,
    trim:true,
    required:true
  },
  amenities:{
    type:[String],
  },
  available_room:{
    type:String
  },
  createdAt:{
    type:Date,
    default:Date.now()
  },
  updatedAt:{
    type:Date,
  },
  ownerId:{
    type:mongoose.Schema.ObjectId,
    ref:'User',
    required:true,
  }
},{timestamps:true})

